
package com.mycompany.ex2.Ex2;

public class Venda{
    private String mes;
    private float valor;

    public Venda(String mes, float valor){
        this.mes = mes;
        this.valor = valor;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }
    
    
    
    
    
    
}
